<h1>About the database</h1>

<p>Barrett Klein (barrett[at]pupating.org) is an Associate Professor of Entomology and Animal Behavior at University of Wisconsin in La Crosse. He heads the <A HREF="http://pupating.org" target=“_blank”>Pupating Lab</a>, which is building this searchable database of cultural entomology from Barrett Klein&apos;s personal collection, from visitor <a href="http://culturalentomology.org/submit.php" target=“_”>submissions</a>, and from numerous repositories on the Internet.</p>

<p>"Cultural entomology studies the reasons, beliefs, and symbolism behind the inclusion of insects within all facets of the humanities."-Dexter Sear (Cultural Entomology Digest, 1993).</p>

<p>Cultural entomology is &quot;the branch of investigation that addresses the influence of insects (and other terrestrial Arthropoda, including arachnids, myriapods, etc) in literature, language, music, the arts, interpretive history, religion, and recreation.&quot; - Charles Hogue (CE Digest, 1993) </p>

<p>Humans have included insects in art for millennia, either symbolically, literally or physically (insect products or bodies as art media). The practice of incorporating insects in art is one of my cultural entomology interests.</p>

<p>Insects Incorporated is accepting submissions with the goal of establishing the most comprehensive, manually annotated cultural entomology database in the world. 
We anticipate that it will be a valuable resource for research and for education at all levels, and that it will also be of general interest to a wide audience on the Internet.</p>

